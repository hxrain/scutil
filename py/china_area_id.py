def query_first_id(addr, offset=0, max=15, area_ids=map_area_ids):
    """从addr的首部开始,严格的查找匹配的第一个地域名对应的行政区划代码集合.返回值:(名称,区划代码集合)或('',None)
        最大遍历限定15个字,是为了匹配最长的地名'积石山保安族东乡族撒拉族自治县'
    """
    tlen = len(addr) - offset + 1
    for pos in range(2, tlen):
        if pos > max:
            break
        area = addr[offset:offset + pos]
        if area not in area_ids:
            continue
        if pos < tlen - 1 and addr[offset:offset + pos + 1] in area_ids:
            continue

        return area, area_ids[area]
    return '', None


def query_area_ids(addr, min_words=2, segs=None, map_areas=map_area_ids):
    """查询addr地址串对应的具体的行政区划代码.返回值:[{区划id}]
        segs可以为list实例,用于记录匹配的每一段名称的范围
    """
    if not addr:
        return []

    addr_size = len(addr)
    begin = 0

    def take():
        """从当前begin位置开始对addr进行最大化匹配查找.返回值:找到的最后匹配位置,或0未找到"""
        for pos in range(addr_size, begin + min_words, -1):
            # 先倒着查询最长匹配串
            if addr[begin:pos] in map_areas:
                return pos

        end = 0
        for pos in range(begin + min_words, addr_size + 1):
            # 再正向逐步试探增量匹配
            if addr[begin:pos] in map_areas:
                end = pos  # 当前文本段是一个标准的区划名称,记录结束位置
            elif end != 0:
                break
        return end

    def is_bad_suff(pos):
        """检查addr[pos:]是否为无效后缀特征"""
        if len(addr) - pos >= 2:
            if addr[pos:pos + 2] in {'社区', '街道', '小区', '校园', '学校', '小学', '中学', '大街', '东路', '西路', '南路', '北路'}:
                return True
        if len(addr) - pos >= 1:
            if addr[pos] in {'路', '街'}:
                return True
        return False

    ids = []
    while begin < addr_size:  # 对addr进行完整的前后扫描,尝试获取省市区划的各个部分.
        end = take()
        if end == 0:
            begin += 1
        else:
            if not is_bad_suff(end):
                s = map_areas[addr[begin:end]]
                ids.append(s)  # 找到了匹配的区划名称,记录对应的区划代码集合
                if segs is not None:
                    segs.append((begin, end))  # 记录匹配的分段范围
            begin = end

    return ids


def query_area_id(addr, min_words=2, map_areas=map_area_ids):
    """查询addr地址串对应的具体的行政区划代码.返回值:(优先结果,疑似结果)
       优先结果为None未找到;疑似结果不为[],说明地址串包含多个区划名称.
    """
    ids = query_area_ids(addr, min_words, map_areas=map_areas)
    if len(ids) == 0:
        return None, None

    def merage(ids):
        # 对匹配的区划代码集列表进行前后叠加检查合并
        rst = ids.pop(0)
        while len(ids):
            res = check_depen(rst, ids[0])
            if res is None:
                # 前后不依赖
                break
            else:
                # 前后依赖,以当前位置继续
                rst = set(res)  # 有依赖的后项为结果
                ids.pop(0)
        return rst

    if len(ids) == 1:
        lst = list(ids[0])
        m = max(lst)
        lst.remove(m)
        return m, lst
    else:
        lst = []
        while (len(ids)):
            aids = merage(ids)  # 前后迭代判断,得到一个行政区划代码集
            lst.append(max(aids))  # 每个区划集中都选取最大值,落实到区县
        return lst.pop(0), lst


def analyse_area_id(txt):
    """分析猜测txt文本中出现次数最多的行政区划id.不存在时返回None"""
    """分析猜测txt文本中出现次数最多的行政区划id.不存在时返回None"""
    lines = txt.split('\n')
    main_dcts = {}
    othr_dcts = {}

    def rec(key, dcts):
        # 累计key的出现次数
        if key not in dcts:
            dcts[key] = 0
        dcts[key] += 1

    for line in lines:
        # 逐行分析文本中出现的区域代码
        mr, olst = query_area_id(line)
        if mr is None:
            continue

        rec(mr, main_dcts)  # 累计主结果
        for sr in olst:
            rec(sr, othr_dcts)  # 累计副结果

    # 按累计数量进行主副结果的倒序排列
    mains = sorted(main_dcts.items(), key=lambda x: (x[1], x[0]), reverse=True)
    othrs = sorted(othr_dcts.items(), key=lambda x: (x[1], x[0]), reverse=True)

    if len(mains) == 0 and len(othrs) == 0:
        return None  # 主副结果均为空
    if len(othrs) == 0:  # 副结果为空,使用主结果
        return mains[0][0]
    if len(mains) == 0:  # 主结果为空,使用副结果
        return othrs[0][0]

    # 主副结果都不为空,则选择出现数量多的结果值
    if mains[0][1] >= othrs[0][1]:
        return mains[0][0]
    else:
        return othrs[0][0]


def areas_split(txt, seps=None, skip_head=True, skip_tail=True):
    """将输入文本行txt按给定的区划分隔符进行拆分.返回值:[(begin,end,'区划名称')]"""
    rst = []
    tlen = len(txt)
    begin = 0
    if seps is None:
        seps = {'省', '市', '区', '县', '州', '盟', '旗', '乡', '村', '镇'}

    def take(begin, end=None):
        """获取指定范围的文本段,跳过前后可能的括号"""
        if end is None:
            end = tlen
        if txt[begin] in {'(', ')'}:
            begin += 1
        if txt[end - 1] in {'(', ')'}:
            end -= 1
        rst = txt[begin:end]
        if rst in {'街道', '社区', '农村', '城市', '乡村', '城乡', '城镇', '都市'}:  # 单纯的分隔符,不输出
            return ''
        return rst

    def loop(pos):
        """从txt的pos开始,查找下一个区划分隔符.返回值:下一个区划分隔符的结束点"""
        bpos = pos
        while pos < tlen:
            if txt[pos:pos + 5] in {'唐市古镇区'}:  # 特例,你咋就不能好好起名字
                return pos + 5
            if txt[pos:pos + 4] in {'唐市古镇'}:
                return pos + 4
            if txt[pos:pos + 2] in {'街道', '社区', '村村', '镇镇', '市村'}:
                return pos + 2
            char = txt[pos]
            next = txt[pos + 1] if pos < tlen - 1 else ''
            if char in seps:
                if pos - bpos <= 1:
                    pos += 1  # 跳过特殊名称
                    continue
                if next in seps and next != char:
                    return pos + 2
                return pos + 1
            pos += 1
        return tlen

    while begin < tlen:
        end = loop(begin)
        seg = take(begin, end)
        if not seg:
            break
        rst.append((begin, end, seg))  # 记录当前段内容
        begin = end

    if skip_head:  # 如果要求跳过头部非地区段(要求进行省市区县合理性检查)
        while rst:
            seg = rst[0][2]
            if query_ids_by_area(seg) is None:
                rst.pop(0)  # 则进行前置循环丢弃检查
            else:
                break  # 遇到地区段了,结束

    if skip_tail:  # 如果要求丢弃尾部非地区段(要求进行尾部段特征检查)
        while rst:
            seg = rst[-1][2]
            if seg[-1] not in seps:  # 不是地区名称则丢弃
                rst.pop(-1)
            else:
                break  # 遇到地区了,结束

    return rst
