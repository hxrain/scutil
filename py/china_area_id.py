def is_depen(ida, idb):
    """分析idb从属依赖ida的相关性;返回值:(a,b),错误为(None,None);
        a=0不相关;1同省;2同市;3同区县
        b=0正常;1省级范围倒置;2不同市;3市级范围倒置;4不同区
        (0,0)不相关;(0,1)省级范围倒置;(0,3)市级范围倒置;
        (1,0)省属市;(1,2)同省不同市;
        (2,0)市属县;(2,4)同市不同区;
        (3,0)同区县
    """
    if isinstance(ida, int):
        ida = str(ida)
    if isinstance(idb, int):
        idb = str(idb)
    if len(ida) != 6 or len(idb) != 6:
        return None, None

    if ida == idb:
        return (3, 0)  # 同区县

    if ida[:2] != idb[:2]:
        return (0, 0)  # 不同省

    a = ida[2:]
    b = idb[2:]
    if a != '0000' and b == '0000':
        return (0, 1)  # 范围倒置,不相关
    if a == '0000' and b != '0000':
        return (1, 0)  # 省属市

    if ida[:4] != idb[:4]:
        return (1, 2)  # 同省,不同市

    a = ida[4:]
    b = idb[4:]
    if a != '00' and b == '00':
        return (0, 3)  # 范围倒置,不相关
    if a == '00' and b != '00':
        return (2, 0)  # 市属区县

    if ida != idb:
        return (2, 4)  # 同市,不同区

    return (3, 0)  # 同区县


def make_map_area_ids2():
    """定制的省市名称构建器:只对自治区/省/市/州两级区划进行名称简化处理."""
    rst = {}

    def check_depen(s, b):
        """检查区划代码b是否为区划代码集合s中的子区县"""
        for a in s:
            cr = is_depen(a, b)
            if cr in {(1, 0), (2, 0), (3, 0), (2, 4)}:
                return True
        return False

    def drop_tail(name):
        nlen = len(name)
        if nlen <= 2:
            return name
        if name in fixed_areas:
            return name  # 特定地名不进行简化处理
        if nlen >= 5 and name[-3:] in {'自治县', '自治州', '自治区'}:
            return name[:-3]  # 丢弃尾三字
        if name[-1] in {'省', '市', '州'}:
            return name[:-1]  # 丢弃单尾字
        if len(name) >= 4 and name[-2:] != '新区':
            if name[-1] in {'区', '县'}:
                return name[:-1]  # 4字以上的区县,可以生成简化名称
        return name

    def _rec(name, id):
        if name not in rst:
            rst[name] = {id}  # 未记录过的名字,直接记录
            return
        if id in rst[name]:  # 区划id与名字重复的,不记录
            return
        if check_depen(rst[name], id):
            # print(name, rst[name], id)
            return  # 子区域和父区域重名,不记录
        rst[name].add(id)  # 重名但不是从属的区划id ,记录

    for id in map_id_areas:
        alst = map_id_areas[id]
        for name in alst:
            _rec(name, id)
            _rec(drop_tail(name), id)

    return rst


