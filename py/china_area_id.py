def calc_aera_level(name):
    """查询指定区域名称对应的行政级别.返回值:0未知;1~5对应级别"""
    if not name:
        return 0
    if len(name) == 3 and name in areas_level:
        return areas_level[name]
    if name in {'北京', '天津', '上海', '重庆'}:
        return 1
    for p in [-3, -2, -1]:
        tail = name[p:]
        if tail in areas_level:
            return areas_level[tail]
    return 0


