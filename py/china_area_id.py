def grouping(ids):
    """对给定的区划代码集合进行省市分组,返回多级分组字典{}"""
    grps = {}
    for i in ids:
        id = str(i) if isinstance(i, int) else i
        PC = id[:2]  # 省份代码
        CI = id[2:4]  # 市级代码
        if PC not in grps:
            grps[PC] = {}  # 按省分组
        if CI not in grps[PC]:
            grps[PC][CI] = set()  # 按市分组
        grps[PC][CI].add(i)
    return grps


def check_depen(aids, bids):
    """查找b区划代码集合中依附a区划代码集合的从属代码,返回值:[区划代码]或None"""
    dct = {}
    for bid in bids:
        for aid in aids:
            d = is_depen(aid, bid)  # 检查b与a的从属相关性
            fd = d[0]
            if fd:
                if d[1]:
                    continue
                if fd not in dct:
                    dct[fd] = []  # 根据从属相关性等级进行结果记录
                dct[fd].append(bid)

    if len(dct) == 0:
        return None

    for l in range(3, 0, -1):
        if l in dct:
            return dct[l]  # 对筛选结果进行从属相关性的高优先级过滤输出
    return None


