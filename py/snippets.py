# 标点符号与停用词
PUNCTUATIONS = {'“', '”', '、', '！', '!', '|', '：', '，', '；', '。', ':', ',', ' ', '\\', '#', '&', '/', '<', '>', '+',
                ';', '·', '×', '—', '‘', '’', '…', '《', '》', '『', '』', '【', '】', '（', '）', '(', ')', '[', ']', '{', '}',
                '？', '`', '~', '～', '@', '#', '\t', '\'', '=', '「', '」', '・', '_', '.', '●', '«', '»',
                '㎡', '﹙', '﹚', '．', '"', '／', '?', '＋', '－', '•', '%', '〔', '〕', '＃', '^', '*', '$', '-', '\u200b',
                '\u200c', '\u200d', '\xa0', '％', '\u3000', '\u200d', '＆', '丶', '\ufeff', '\x7f', '\x10'}


def drop_punctuations(in_str, punctuations=PUNCTUATIONS):
    '''删除给定字符串中的标点符号'''
    out_list = []
    for c in in_str:
        if c in punctuations:
            continue
        out_list.append(c)
    return ''.join(out_list)


print(drop_punctuations('abcdcesh测试！~一下下《》“”！'))
